def main():
    print("загружаем пайплайн...")
    pipeline = RAGPipeline(
        lm_studio_url=LM_STUDIO_URL,
        model_id=LM_STUDIO_MODEL_ID
    )
    print("пайплайн готов\n")

    for i, text in enumerate(TEST_TEXTS, start=1):
        print(f"\n{'#' * 70}")
        print(f"# тест {i}/{len(TEST_TEXTS)}")
        print(f"{'#' * 70}")
        print(f"исходный текст: {text[:120]}...\n")

        result = pipeline.run(text)

        print(f"\n>>> ИТОГОВЫЙ ВЫВОД <<<")
        if result.get("status") == "ok":
            r = result.get("result", {})
            print(f"  source: {result.get('source', '')}")
            print(f"  Вход:   {r.get('input_params', '')}")
            print(f"  Объект: {r.get('object', '')}")
            print(f"  Выход:  {r.get('output_params', '')}")
        else:
            print(f"  ошибка: {result.get('message', '')}")
        print()


if __name__ == "__main__":
    main()